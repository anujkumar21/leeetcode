package com.leetcode.dynamic_programing;

public class LongestCommonSubsequenceBottomUp {

    public int longestCommonSubsequence(String text1, String text2) {
        int N = text1.length();
        int M = text2.length();
        int[][] dp = new int[N + 1][M + 1];


        return dp[0][0];
    }


}
